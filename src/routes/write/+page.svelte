  <h2>On wanting to write</h2>
  <p>
    You probably have experience with desire... god knows i've had my share. Sometimes i crave
    something sweet, sometimes savory, sometimes i want chaos, and other times a little rest. Lately
    i've been playing with the idea of breathing new life into this website. To accomplish this i
    need to dodge shame for my blazing mediocrity and percieve this website more like a digital
    garden full of half finished pet projects. I think i would like to start writing a few essays
    but how do i set myself to writing, or rephrased, how do i fullfill my desire of wanting to
    write, or even wanting to be someone who'd want to write... This is not a craving easily
    satisfied. In philosophy we like to nitpick about things like essences so of course we
    distinguish multiple orders of desire.<sup><a href="#wr-fn1" id="wr-ref1">1</a></sup>
    Why bother keeping things simple when we can complexify beyond comprehension, right? My desire to
    write essays sounds like it might be a second or third order desire, let's explore what this means.
  </p>
  <p>
    First order desires are like the things i described in the first few sentences, you 'want'
    something that is easliy satisfied: Either you decide to act on your craving and get that
    sweettooth it's share, or you don't and work on flattening curves instead. Second order cravings
    are one step removed from actual experience and they describe things like: i wish i wouldn't
    crave for sweets or - the whole reason for writing here - i wish i'd write more. Second order
    desires can be conflicting and therefore dificult to deal with. Third order desires are way out
    there and feel a bit like placing accountability outside yourself, desires like: i wish i was a
    person who'd want to write more. There is discussion as this may lead to an infinite regression,
    we could for instance think up a fourth-order desire in which i would want to be a person would
    want to be a person who'd want to write more, or a fifth..., sixth... etcetera ad infinitum.
  </p>
  <p>
    Infinite regressions are fun but crash computers as well as brains.. But the problem perists,
    how do i set myself to writing? Luckily, for me, there is a surprisingly easy and fullfilling
    answer to this problem: Love!<sup><a href="#wr-fn2" id="wr-ref2">2</a></sup> We can't always decide
    what we desire, sometimes it just happens; we feel love for something or someone. This love precedes
    rationality and gives us reasons to do things. Therein lies a sollution, you don't have to analyse
    your desires ad infinitum, there simply is stuff you love. The only thing that keeps me from doing
    things is actually reserving time for it, to accomplish that i need to decide if i love this particular
    thing enough to set myself to do it.
  </p>

