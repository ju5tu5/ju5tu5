  <h2>Four Words: The Web You Want</h2>

  <p>
    The instigator for this essay is Vasilis van Gemert who had the brilliant idea to organise a
    conference called ‘The Web You Want’ at the Amsterdam University of Applied Sciences. Me being
    me - computer scientist/ philosopher/ lecturer - i immediately started tripping on the title:
    Why these exact choice of words? What does it all mean? This is due to an acquired philosophical
    tendency to complexify everything i encounter beyond comprehension, looking for clarity i might
    never reach. However, the journey is more important than the destination… right? I’d like you to
    tag along on this trip along four words in which i will ask the following questions and, within
    given time, search for possible answers.
  </p>

  <h3>Four questions</h3>

  <ul>
    <li>Is this conference really about ‘the’ web, or could it be about ‘a’ web?</li>
    <li>
      What is the content and extent of the word ‘web’ and what meaning is attached to it in this
      context?
    </li>
    <li>
      Is it really ‘me’ being addressed through ‘you’ or is there a hidden agenda in Vasilis’ choice
      of words and what happens if ‘you’ is changed to ‘they’?
    </li>
    <li>Want expresses a desire… How can we understand this desire?</li>
  </ul>

  <p>
    Let’s ponder on some of the possible answers in realisation that we will never find a definitive
    answer, it wouldn’t be philosophy otherwise. We will conclude on a lighter note leaving all of
    us with an insurmountable task, great!
  </p>

  <h3>Is this conference really about ‘the’ web, or could it be about ‘a’ web?</h3>

  <p>
    In the profound English language we are bound to use two kinds of articles, a definite ‘the’ or
    indefinite ‘a(n)’. The definite variant was chosen in the sentence we’re researching and of
    course i wonder what would happen if we’d use the indefinite article instead. Is there
    difference in usage? This might seem like insignificant nitpicking but there is a big
    philosophical difference between these seemingly similar terms. This difference touches on
    philosophical questions about (the possibility of) knowledge, identity, existence and the way we
    structure the world. I for one, indoctrinated by postmodern philosophers, prefer a pluriverse of
    possibility above the one-world-world. How about you? Let’s see what we can find…
  </p>

  <p>
    ‘The’ is the definite article, symbol of the specific. Something unique and already known, or
    that can be identified. It implies (or supposes) a shared understanding between the speaker/
    writer and the listener on what is meant. eg. ’THE apple’ in the sentence, ‘please hand me the
    apple’, points towards a specific apple that both parties know. So ‘the’ presumes a shared
    context of knowledge implicating that the speaker/writer and listener are in the same “world” in
    which the named object already exists. Programmatically speaking, “the” variable - probably a
    constant - is named/ declared/ baptized and already bound to a specific value. This can be taken
    as a form of intersubjectivity, a shared understanding between people. ‘The’ signifies something
    that is not just ‘a’ thing but ‘that’ specific thing. There is a certain direction in attention
    on both sides of the utterance. An acknowledgement of the existence and unique nature of an
    object with a specific place in reality. Reflecting a deterministic outlook on the
    one-world-world, adhering a solidified theory of everything.
  </p>

  <p>
    ‘A(n)’ is something completely different as it signifies something general and indefinite. A
    thing that might be part of this or that category but isn’t exactly unique. It implies that the
    specific object is not yet identified. eg. ‘AN apple’ in the sentence ‘please hand me an apple’
    means any other apple without pointing to a specific one. This philosophically reflects a
    tension between the particular and the general. ‘The’ points towards the concrete and unique,
    while ‘a’ points to the abstract and general. It introduces something new, something that was
    unknown within the context. It opens the door to possibility and potential without connecting a
    specific meaning. There is an unbound variable waiting to be connected to a specific value. ‘A’
    points towards possible futures, it represents an open and insecure world, where things aren’t
    completely determined, ‘a’ pluriverse in which we get to fill in the yet unbound future..
  </p>

  <p>
    We can interpret this as a reflection of epistemology, the study of knowledge: ‘the’ is about
    what we know while ‘a’ is yet to be discovered or interpreted. It’s the difference between what
    is actual and what is potential. Seeing this explanation i’d rather work on discoverable future
    possibilities of ‘a web’ than being stuck in the known cybernetic crevices of our commercially
    exploited and weaponized ‘the web’. Language being the utterly weird thing it is we could, with
    Hans Reichenbach, make a time shift and agree to use the word ‘the’ to describe a potential
    future work of perfection. But in my mind, this would be totalitarian, i want discoverability,
    the openness of a future pluriverse in which things we cannot even imagine are possible. But
    let’s not get ahead of ourselves and jump to conclusions after the first small exercise but
    instead continue with the second word, ‘web’.
  </p>

  <h3>
    What is the content and extent of the word ‘web’ and what meaning is attached to it in this
    context?
  </h3>

  <p>
    Excavating the meaning of a word is a very Foucaultian thing to do but before getting out
    “power”-tools and drilling down we need a small background story about philosophy of language.
  </p>

  <p>
    There is this Austrian high-class guy called Wittgenstein who wrote the <em
      >Tractatus Logico-Philosphicus</em
    >, arguing how language only makes sense if there is a testable connection to observable things.
    This so called ‘correspondence theory of language’ roots out religion and metaphysics as
    philosophizable concepts because by nature there can’t ever be concrete answers. A lot of
    philosophers in the Anglo-Saxon world embraced this idea naming their philosophy ‘analytic’ as
    opposed to more European focused ‘continental’ philosophy. However, nudged by Gödels
    incompleteness theorem, Wittgenstein slowly realised that he himself was actually pretty
    continental, so he changed his mind and wrote
    <em>Philosophische Untersuchungen</em>, in which he states that he used to be utterly wrong.
    Language gets its meaning in use and words have value in the way they are used. In fact
    everything we do is explainable as an engagement in language games, like the one we are in right
    now. Let that sink in…
  </p>

  <p>
    Back to Foucault.. With similarities to Wittgenstein he researches truth-games and shows how
    power, knowledge, and discourse mutually shape one another, thereby determining what is
    considered as true, normal, and possible in a given society. For him a “word” is never just a
    neutral label but a node where history, power, and knowledge converge. “Words” nudge their users
    towards certain kinds of behaviour through a process called normalization. In other words, and
    channeling McLuhan, we shape our <del>tools</del> languages and in turn, our <del>tools</del> languages
    shape us.
  </p>

  <p>
    Let’s follow in Foucault’s wake and explore the everyday use of the word “web” to see if we can
    excavate hidden power structures. Occupational bias forces me towards the WWW but of course
    there are a lot of everyday physical conceptions of the word ‘web’, like a spider web, other
    woven material and figurative uses like web of lies. Let’s see what secrets lie in the heart of
    these everyday uses stating deliberately that what follows is in no way individually necessary
    and jointly sufficient to capture all possible uses of the word ‘web’.
  </p>

  <p>
    Spider webs are intriguing artefacts of insect engineering. Networks of silk threads spun with
    purpose: mainly trapping prey, but in some species providing shelter for eggs or flying around
    and i’m not even talking about Spiderman. Spider silk is lightweight, flexible and incredibly
    strong. Different types of silk are used for different parts of the web, such as sticky silk for
    catching prey and non-sticky silk for structural support. Spider webs come in many shapes
    because spiders adapt their web to the environment and their needs. Some are funnel shaped, some
    are planes that are smaller and sturdier in windy areas, larger and more spread out in dense
    vegetation. Let’s see what insights we can gain using the spider web as analogy for the WWW.
  </p>

  <p>
    A web is a network, not a hierarchy or a list but a distributed mesh of interconnected nodes.
    Just like silk threads connect anchor points across a physical space, hyperlinks connect
    resources across the digital. Unlike some spiderwebs the WWW has no single centre; the topology
    emerges from countless individual acts of “spinning”. The WWW is the product of billions of
    independent contributors with no single coordination, arguably its most interesting
    characteristic.
  </p>

  <p>
    Spider silk is disproportionately resilient for its weight, it doesn’t collapse when one or two
    threads are broken. There are however structural threads that when broken, trigger a fold of
    part of the web. The WWW achieves similar robustness through redundancy and distribution, the
    original ARPANET was built from the principle that a network should survive node failure by
    rerouting. In similarity with the spider web there are some extra special nodes (eg AMS-X) that
    might cut off parts of the WWW. There is another form of adaptiveness in the WWW, the subtle art
    of responsiveness through which we build stuff that will display nicely even though we don’t
    know what device it will run on.
  </p>

  <p>
    ‘Webs’ are purpose built with intent, the main purpose of a spiders web is trapping prey, I
    wouldn’t go as far and say that the main purpose of the WWW is exactly the same but its
    architecture does encode intent: pages, APIs, apps and services are designed, built and
    published to serve particular goals. You can certainly see how these days humanity uses a
    disproportional amount of websites purposefully designed to catch us in their funnels, influence
    our behaviour, mine our attention or steer us towards a transaction. It seems like no
    psychological dark pattern is left unused to capture and guide visitors towards certain
    behaviour.
  </p>

  <p>
    Aside from spiders physical manifestation on earth the word ‘web’ holds other everyday uses with
    woven material on different levels of abstraction. A web can be an artifact like a weave or
    mesh.
  </p>

  <p>
    In the textile industry the word web is used in quite a few different ways. The pre-yarn fiber
    sheet in which fibers are spread to hook into each other before being spun into threads is
    called ‘web’. After ‘spinning’ - ’spin’ is the dutch word for spider… - threads are put together
    in a proces called knitting or weaving to form fabric. It can be a tightly woven fabric such as
    canvas, jeans or denim which creates a surface or plane, but it can also be knitted into an open
    structure that makes it more mesh like, for example in netting, gauze, tulle or lace. In both
    cases you can see traces of the process of creating, the warp-and-weft decisions made during the
    process that characterize the finished product. Warp are the fixed longitudinal threads held
    under tension while weft are the mobile threads that pass through or transverse the warp.
  </p>

  <p>
    Just like in the study of fabric we can dive in to the structure of the WWW and look at in what
    way specific area’s of the web are connected. We can think of a conglomerate of sites that link
    to all other sites within the conglomerate creating a tight structure, or a loose ring where
    sites link to exactly one other site looping the last in line to the first site, or a trap on
    one specific platform using javascript to prevent navigating away. What other forms/ shapes
    (morphe) can you come up with! I’d very much like to hear creative ways of describing a flock/
    gathering/ murder of sites!
  </p>

  <p>
    Aside from representing structure fabric is also used as surface. Think of T-shirts with a clear
    political message or ancient royal tapestries depicting heroic deeds. In tapestries as in some
    high quality woven shirts, the image on the front of the fabric is inseparable from the
    structural logic on the back as the story is woven into the very fabric. In lower quality fabric
    stories degrade over time, through washing the image fades and is forgotten, traverses into
    myth, as is the case with every band shirt i’ve ever owned.
  </p>

  <p>
    The analogy is clear, the WWW entails both a structure and a surface to carry meaning. On most
    commercial sites, meaning is highly transitory, sites that consist of some kind of feed that
    follows fads and fashions and flows like a current. Like band shirts, we seem to have a weird
    kind of fascination with these volatile surfaces as they are deliberately designed to speak to
    our psyche. Just look around you in public transport to witness humanity’s fascination with the
    ‘fleeting web’. Luckily there are sites on which meaning is more solidified, or static. Well
    designed sites, artefacts where structure and surface tie into each other, just like in
    historical tapestries. These are places you remember, sites you revisit because the meaning
    carried over resonates deeply within you.
  </p>

  <p>
    A third use of woven fabric lies in the word ‘webbing’ which describes a mesh structure of
    tightly interwoven straps that binds/holds meaningful stuff together, for instance in cars or
    planes. Webbing takes care of the stuff we cherish, making sure it survives our rough journeys.
    We can look at hyperlinks and depict them as the ‘webbing’ of the web, taking care to use it as
    a noun and not a verb. That would be preposterously neo-platonic, talking about the ‘webbing of
    the web’, holding the view that in the intelligible world there must exist a perfect version of
    the web to which the web available in our sensory world will always be an imperfect shadow. Or
    might this hypothetical metaphysical perfection be the web in Vasilis’ intent?
  </p>

  <p>
    Then there are figurative uses of the word ‘web’. Echoing the spiders cunning manifestation we
    can also find ourselves caught in a web of lies or intrigues, but let’s not linger for too long.
  </p>

  <h3>
    Is it really ‘me’ being addressed through ‘you’ or is there a hidden agenda in Vasilis’ choice
    of words and what happens if ‘you’ is changed to ‘they’?
  </h3>

  <p>
    These are actually three questions wrapped into one. Let’s take look at the third part, what
    happens if the pronoun ‘you’ is changed to ‘they’? Using ‘they’ is unbound in the same way that
    ‘a’ is unbound. We can use the word and hook it up to any selection of things:
  </p>

  <ul>
    <li>
      ‘They’ the tech-feudalists, extorting humanity, mining attention, squeezing every last penny
      out of us
    </li>
    <li>‘They’ the web-builders, creating fancy sites that can only be used on fancy computers</li>
    <li>
      ‘They’ the users, with all their differences, peculiarities and preferences, the web that
      accommodates everyone. I think this is what TBL meant when he stated: This is for everyone,
      but somehow mainstream webdevelopers are failing miserably.
    </li>
    <li>
      ‘They’ algorithms, i remember a time when we just built website, later we built websites with
      SEO in mind and if i follow some of the speakers at SmashingConf we have to lint the fruits of
      our labour to accommodate to AI algorithms to achieve perfect design to code bridges
    </li>
    <li>‘They’ the trees, reporting growth through sensor networks</li>
    <li>‘They’ the squirrels, having their privacy violated by webcams while going nuts</li>
    <li>‘They’ the population of Dodo’s before the arrival of settlers</li>
  </ul>

  <p>
    We see that difficulty arises from using ‘they’ because ‘they’ can entail every selection of
    entities we can come up with. Although i like possibility and inclusivity this approach leads us
    into infinite complexity. We already encounter a lot of problems building ‘a’ web for
    every’one’, how would we even set out to design a web for every’thing’?
  </p>

  <p>
    Let’s leave this thought here and look at the part first question. It is about all the things
    signified by ‘you’. If ‘i’ read this title, ‘you’ addresses ‘me’. ‘You’ might also address any
    other person in this audience. Who is it? Vasilis, please enlighten us? But ‘you’ might also
    address a group of people, maybe even the whole audience and can even be used as a directive.
  </p>

  <p>
    ‘You’ might signify ‘me’ and this aligns perfectly with Vasilis’ exclusive design approach. ‘<em
      >The</em
    >
    web’,
    <em>my</em> web, real connections with real people devoting very real time to real content. Everything
    i open actually interesting to read, for me! Enter the ultimate ‘Justus’ bubble, no more generated
    slop, no commercial traps mining my attention, just quality ASCII-arted content perfectly aligned
    with my personal interest grid and never… any… banner… Maybe there’s something in it for you, maybe
    not, i don’t care, my web. This sounds wonderfully scary and very solopsistic but we are actually
    algorithmically forced into bubbles on a daily basis and judging by numbers we seem to enjoy it. I
    don’t know about you, but i would very much like to meet Others and have my assumptions destroyed,
    preferably replaced with new insights based on better arguments. Please be gentle, not all my assumptions
    at once…
  </p>

  <p>
    ‘You’, might also mean you, any other person in the audience, imagine the web YOU want. The
    question arises if there will be anything appealing to ‘my’ specific taste inside ‘your’ bubble.
    But this would also form a philosophical problem called the ‘problem of other minds’. If the web
    is made especially for you, and others are a part of creating it, then there must be something
    it is like to be you? Nagel might want a few words with you if you actually think there is.. and
    even if there is, can ‘Others’ actually experience what it is like? This ‘Otherness’, what
    Levinas calls ‘alterity’, lies in the fact that Other people are infinitely beyond
    comprehension. We simply can’t grasp the infinitely transcending Otherness they represent. That
    is what makes the web fun, being amazed by all these wonderful people with weird fascinations.
  </p>

  <p>
    ‘You’ can also address a group of people and might even address a part or even all of you, this
    audience as a whole. This conference is called ‘the web you want’ and here you are. The thing
    being talked about here is probably something you actually want or you wouldn’t be here! The
    ‘you’ in the title combined with the ‘want’, might have spoken to your <em
      >fear of missing out</em
    > (FOMO). Could this be a perfect marketing strategy conjured to fill these seats? Picture Vasilis
    caressing his copy of Cialdini, gloating over all you people that actually turned up. Maybe he even
    meant to use ‘you’ in a directive way, demanding that what is presented here today should be the actual
    web we want… Well, let me tell you, I’ve worked with Vasilis for some time now, long enough to conclude
    that this could never be his intention.
  </p>

  <p>
    The last option is that ‘you’ is used to address us as a group of people, but in a special way
    that targets each one of us individually. If taken this way, ‘the web’ in question would have to
    accommodate everyone and could never be a singular thing. This view resonates with the
    pluriverse and might hold enough explanation to settle on for now.
  </p>

  <h3>Want expresses a desire… How can we understand this desire?</h3>

  <p>
    As you can probably imagine, there is a lot of philosophy on the subject of desire as it entails
    an important human faculty. I’ll rush by a few french philosophers trying not to make things too
    abstract.
  </p>

  <p>
    For Lacan, a psychoanalytic, desire comes from a fundamental &#8220;lack&#8221; or
    incompleteness. Desire is a not drive towards satisfaction but a perpetual movement around
    absence, rooted in the fact that as soon as we learn language we lose unmediated access to
    ourselves and to the world. Desire tries to fill that gap by fixating on a goal, the thing that
    promises satisfaction is called <em>l’objet petit a</em> which is not a real thing but a fantasy
    placeholder that keeps desire in motion. This is not a desire you have but a desire that has
    you, and satisfaction will always displaced or somehow off. That is why desire immediately
    relocates to the next thing. The Lacanian approach is not to satisfy your desires but to
    transcend them. Traversing the fantasy and recognize our <em>l’objet petit a</em> as structural
    placeholder and start relating to our lack instead of being tyrannized by it. Through this
    process our relation towards incompleteness changes towards <em>désir assumé</em>, assumed or
    accepted desire knowing that it will never be fulfilled but will be a life-work.
  </p>

  <p>
    Sartre holds the view that desire is a key part of human freedom and subjectivity, the spark
    that sets us into motion, that makes us project ourselves into the future. However, Sartre warns
    of ‘bad faith’, where we pursue desires imposed by external sources rather than those that
    reflect our authentic self. With Sartre we can ask if ‘the web you want’ entails a desire that
    is truly your own or a wish/ dream or that it is shaped by societal expectations?
  </p>

  <p>
    According to Girard there is no such thing as authentic desire because our desires are always
    shaped by the desires of others. We want what others want because we imitate their desires in a
    struggle for recognition. Desire is not a direct relation between subject -&gt; object, but a
    triangular one, subject -&gt; model -&gt; object. Copied desires often lead to rivalry,
    conflict, and a &#8220;desire to desire&#8221; and managing them requires, in line with Lacan,
    awareness and an intentional shift of focus, not suppression. We can never fully eliminate our
    mimetic nature, but we can manage its destructive tendencies by moving from unconscious
    imitation to conscious awareness. We can do this by identifying the models underneath our
    desires and ask ‘who’ makes me want this instead of ‘what’ do i want. Then you decide if the
    model is an internal rival telling you that you are not good enough, or a distant idol that you
    consciously hold in awe. You can use these insights to stop fussing over rivalry and start
    actively following worthy role-models.
  </p>

  <p>
    Now that we have a little background in desire we can look at the temporality of ‘the web you
    want’ or in other words, is there a ’when’ connected to the fulfilment of this desire. Frankfurt
    likes to nitpick about these things and distinguishes multiple orders of desire. The ‘want’ing
    of the web sounds like it might be a second or third order desire. In first order desires you
    &#8216;want&#8217; something concrete that is easily satisfied: Either you decide to act on your
    craving and get that sweet-tooth it&#8217;s share, or you don&#8217;t and work on flattening
    curves instead. The overwhelming size of the web hints at the impossibility of being a first
    order desire. Second order desires are more abstract, one step removed from our actual
    experience and describe things like: i wish i wouldn&#8217;t crave for sweets or i wish the web
    would be another place. Second order desires are usually conflicting and therefore much more
    difficult to deal with. Third order desires are way out there and feel a bit like placing
    accountability outside yourself, desires like: i want to be a person who&#8217;d want do things.
    There is discussion on the possibility of third order desire as it is the first step in an
    infinite regression. We could for instance think up a fourth-order desire in which i would want
    to be a person would want to be a person who&#8217;d want something, or a fifth&#8230;,
    sixth&#8230; et cetera ad infinitum.
  </p>

  <p>
    Infinite regressions are fun but crash computers as well as brains.. But our problem persists,
    how do we explain want in our search for clarity? Luckily, the Belgian philosopher Stefaan
    Cuypers (KU Leuven) conjured a surprisingly easy and fulfilling answer to the problem described
    by Frankfurt: Love! We are influenced by others and can&#8217;t always decide what desire or
    choose to follow, sometimes it just happens; we feel love for something or someone. This love
    precedes rationality and gives us reasons to do things we care for. Following Cuypers, the only
    thing that keeps you from realising the web you want is to really embrace the goal and actually
    reserve time to deliver things you are passionate about. This might sounds easy but is one of
    the hardest things to do in the maelstrom of things bombarded at us in the experience of life.
  </p>

  <h3>To conclude</h3>

  <p>
    Congratulations! You made it to the end of this session. Let’s recap the things we found
    close-reading the title of this conference:
  </p>

  <ol>
    <li>
      The distinction between &#8216;the&#8217; and &#8216;a&#8217; encodes a fundamental
      philosophical divide between a deterministic, singular world of fixed knowledge and an open,
      pluralistic universe of possibility and discovery.
    </li>
    <li>
      The word &#8217;web’, whether spun by spiders, woven into fabric, or hyper-linked across the
      digital, reveals a tension between entrapment and openness, between surface and structure,
      encoding the intention of it’s creators.
    </li>
    <li>
      The pronoun ‘you’, whether addressing a singular self, an unknowable Other, or an entire
      audience, ultimately resists reduction to any single referent, mirroring the pluriverse
      itself: a web that can never be one thing for everyone, yet must somehow be everything for
      anyone.
    </li>
    <li>
      Beneath all the philosophical wizardry of lack, bad faith, and mimetic rivalry, the path to
      the web you want turns out to be disarmingly simple: love the web enough to care for it and
      decide how you give it your time.
    </li>
  </ol>

